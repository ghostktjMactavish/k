public class Decisions1
{
   public static void main(String[] args)
   {
      int i = 3;
      switch(i)
      {
         case 1:
            System.out.println("i is 1");
            break;
         case 2:
            System.out.println("i is2");
            break;
         case 3:
            System.out.println("i is 3");
            break;
         default:
            System.out.println("Invalid value");
      }
   }
}